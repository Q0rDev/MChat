package ca.q0r.mchat.types;

/**
 * Enum for Different Event Types.
 */
public enum EventType {
    /**
     * Player Join Event.
     */
    JOIN,
    /**
     * Player Quit Event.
     */
    QUIT,
    /**
     * Player Leave Event.
     */
    LEAVE,
    /**
     * Player Kick Event.
     */
    KICK
}